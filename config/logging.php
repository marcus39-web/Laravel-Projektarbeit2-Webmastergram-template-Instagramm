    | verwendet die Monolog PHP Logging-Bibliothek, die viele leistungsfähige
    | Handler und Formatter bietet, die du frei nutzen kannst.
    |
    | Verfügbare Treiber: "single", "daily", "slack", "syslog",
    |                    "errorlog", "monolog", "custom", "stack"
    |
    */

